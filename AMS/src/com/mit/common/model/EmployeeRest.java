package com.mit.common.model;

import java.io.Serializable;
import java.util.Date;

import net.sf.json.JSONObject;

public class EmployeeRest implements Serializable{

	/**
	 *员工休班信息 
	 */
	private static final long serialVersionUID = 1L;
	private Long id;
	private Long e_id;
	private String e_name;
	private Long status;
	private Long centerid;
	private String e_start_time;
	private String e_end_time;
	private Date addtime;
	private String id_name;
	public String getId_name() {
		return id_name;
	}
	public void setId_name(String id_name) {
		this.id_name = id_name;
	}
	public String toString(){
    	return JSONObject.fromObject(this).toString();
    }
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public Long getE_id() {
		return e_id;
	}
	public void setE_id(Long e_id) {
		this.e_id = e_id;
	}
	public String getE_name() {
		return e_name;
	}
	public void setE_name(String e_name) {
		this.e_name = e_name;
	}
	public Long getStatus() {
		return status;
	}
	public void setStatus(Long status) {
		this.status = status;
	}
	public Long getCenterid() {
		return centerid;
	}
	public void setCenterid(Long centerid) {
		this.centerid = centerid;
	}
	public String getE_start_time() {
		return e_start_time;
	}
	public void setE_start_time(String e_start_time) {
		this.e_start_time = e_start_time;
	}
	public String getE_end_time() {
		return e_end_time;
	}
	public void setE_end_time(String e_end_time) {
		this.e_end_time = e_end_time;
	}
	public Date getAddtime() {
		return addtime;
	}
	public void setAddtime(Date addtime) {
		this.addtime = addtime;
	}
	
}

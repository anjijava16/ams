package com.mit.common.model;

import java.io.Serializable;

import net.sf.json.JSONObject;

/**
 * 标准工时及分中心工时
 */
public class WorkingHours implements Serializable {

	private static final long serialVersionUID = 1L;
	private Long id;
	private String damage_parts_group;
	private String damage_position;
	private String damage_parts;
	private String repairtype;
	private String repairmethod;
	private String taskproject;
	private String repairlevel;
	private String pricerange;
	private String hours;
	private Long price;
	private Long status;
	private Long centerid;//分中心编号
	private Long basicid;
	public Long getBasicid() {
		return basicid;
	}
	public void setBasicid(Long basicid) {
		this.basicid = basicid;
	}
	public Long getCenterid() {
		return centerid;
	}
	public void setCenterid(Long centerid) {
		this.centerid = centerid;
	}
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getDamage_parts_group() {
		return damage_parts_group;
	}
	public void setDamage_parts_group(String damage_parts_group) {
		this.damage_parts_group = damage_parts_group;
	}
	public String getDamage_position() {
		return damage_position;
	}
	public void setDamage_position(String damage_position) {
		this.damage_position = damage_position;
	}
	public String getDamage_parts() {
		return damage_parts;
	}
	public void setDamage_parts(String damage_parts) {
		this.damage_parts = damage_parts;
	}
	public String getRepairtype() {
		return repairtype;
	}
	public void setRepairtype(String repairtype) {
		this.repairtype = repairtype;
	}
	public String getRepairmethod() {
		return repairmethod;
	}
	public void setRepairmethod(String repairmethod) {
		this.repairmethod = repairmethod;
	}
	public String getTaskproject() {
		return taskproject;
	}
	public void setTaskproject(String taskproject) {
		this.taskproject = taskproject;
	}
	public String getRepairlevel() {
		return repairlevel;
	}
	public void setRepairlevel(String repairlevel) {
		this.repairlevel = repairlevel;
	}
	public String getPricerange() {
		return pricerange;
	}
	public void setPricerange(String pricerange) {
		this.pricerange = pricerange;
	}
	public String getHours() {
		return hours;
	}
	public void setHours(String hours) {
		this.hours = hours;
	}
	public Long getPrice() {
		return price;
	}
	public void setPrice(Long price) {
		this.price = price;
	}
	public Long getStatus() {
		return status;
	}
	public void setStatus(Long status) {
		this.status = status;
	}
	public String toString() {
		return JSONObject.fromObject(this).toString();
	}

}

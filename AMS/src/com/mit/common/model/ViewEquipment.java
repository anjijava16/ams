package com.mit.common.model;

import java.io.Serializable;

/**
 * 展示器设备
 *
 */
public class ViewEquipment implements Serializable {

	private static final long serialVersionUID = 1L;
	private Long id;
	private Long status;
	private Long centerid;//分中心编号
	private String view_equipment_id;
	private String view_name;
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public Long getStatus() {
		return status;
	}
	public void setStatus(Long status) {
		this.status = status;
	}
	public Long getCenterid() {
		return centerid;
	}
	public void setCenterid(Long centerid) {
		this.centerid = centerid;
	}
	public String getView_equipment_id() {
		return view_equipment_id;
	}
	public void setView_equipment_id(String view_equipment_id) {
		this.view_equipment_id = view_equipment_id;
	}
	public String getView_name() {
		return view_name;
	}
	public void setView_name(String view_name) {
		this.view_name = view_name;
	}
}

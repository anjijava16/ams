package com.mit.order.bo;

import net.sf.json.JSONObject;


/**
 * T_ASK 实体类 Thu Nov 10 13:45:05 CST 2016 刘鹏飞
 */

public class Ask {
	private Long id;
	private String ask_name;
	private Long center_id;
	private String del_flag;
	private String centerid;

	public void setId(Long id) {
		this.id = id;
	}

	public Long getId() {
		return id;
	}

	public void setAsk_name(String ask_name) {
		this.ask_name = ask_name;
	}

	public String getAsk_name() {
		return ask_name;
	}

	public void setCenter_id(Long center_id) {
		this.center_id = center_id;
	}

	public Long getCenter_id() {
		return center_id;
	}

	public void setDel_flag(String del_flag) {
		this.del_flag = del_flag;
	}

	public String getDel_flag() {
		return del_flag;
	}
	public String toString() {
		return JSONObject.fromObject(this).toString();
	}

	public String getCenterid() {
		return centerid;
	}

	public void setCenterid(String centerid) {
		this.centerid = centerid;
	}
}
